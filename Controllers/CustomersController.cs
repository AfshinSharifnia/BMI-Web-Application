using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.Rendering;
using Microsoft.EntityFrameworkCore;
using BodyMassIndexer.Data;
using BodyMassIndexer.Models;
using static Microsoft.EntityFrameworkCore.DbLoggerCategory;
using Newtonsoft.Json;

namespace BodyMassIndexer.Controllers
{
    public class CustomersController : Controller
    {
        private readonly ApplicationDbContext _context;

        public CustomersController(ApplicationDbContext context)
        {
            _context = context;
        }


        // Search Customers
        public async Task<IActionResult> Index(string Id)
        {
            var bodymasses = from m in _context.Customers
                             select m;

            if (!String.IsNullOrEmpty(Id))
            {
                bodymasses = bodymasses.Where(s => s.LastName.Contains(Id));
            }

            return View(await bodymasses.ToListAsync());
        }


        // GET: Customers
